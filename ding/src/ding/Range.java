package ding;

public class Range {
	

	public static void main(String[] args) {
		//Most Signified value
		byte b = -128;
		byte B = 127;
		System.out.println(b);
		System.out.println(B);
		//Least Signified value
		byte c=0;
		byte C = -1;
		System.out.println(c);
		System.out.println(C);
		//Error
		/*byte i = 1000;
		error will prevail*/
		//integer max range
		int v = 2147483647;
		System.out.println(v);
		float f = 4f;
		System.out.println(f);
		short s =32767;
		System.out.println(s);
	
		
	}
}
